// program to use variable
#include <stdio.h>

int main()
{
    // declarations of all variables
    int a = 23;                 // integar variable
    float f = 23.92;            // float variable
    char ch = 'p';              // character variable
    char str[] = "Pankaj_Rana"; // string variable

    // display all variables values
    printf("The value of integar variable a is %d \n", a);     // integar value
    printf("The value of floating variable f is %0.2f\n", f);  // floating value
    printf("The value of character variable ch is %c \n", ch); // character value
    printf("The value of string variable is %s \n", str);      // string value

    return 0;
}